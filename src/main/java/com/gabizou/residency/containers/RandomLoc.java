package com.gabizou.residency.containers;

public class RandomLoc {

    double x = 0D;
    double y = 0D;
    double z = 0D;

    public RandomLoc(double x, double y, double z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }

    public double getX() {
        return this.x;
    }

    public double getY() {
        return this.y;
    }

    public double getZ() {
        return this.z;
    }
}
